

package day24;


public class Day24 {

    public static void main(String[] args) {
       int[] angka = {1, 2, 3, 4, 5};
       
       for (int i = 0; i < angka.length; i++) {
            System.out.println(angka[i]);
        }
        
       
        for (int a : angka) {
            System.out.println(a);
        }
    }
}
