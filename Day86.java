
package com.mycompany.day86;


public class Day86 {

    public static void main(String[] args) {
        int angkaPertama = 10;
        int angkaKedua = 5;

        // Penjumlahan
        int hasilPenjumlahan = angkaPertama + angkaKedua;
        System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan);

        // Pengurangan
        int hasilPengurangan = angkaPertama - angkaKedua;
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);

        // Perkalian
        int hasilPerkalian = angkaPertama * angkaKedua;
        System.out.println("Hasil Perkalian: " + hasilPerkalian);

        // Pembagian
        int hasilPembagian = angkaPertama / angkaKedua;
        System.out.println("Hasil Pembagian: " + hasilPembagian);

        // Modulus
        int hasilModulus = angkaPertama % angkaKedua;
        System.out.println("Hasil Modulus: " + hasilModulus);
    }
}
