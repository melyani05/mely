
package com.mycompany.day68;


public class Day68 {

    public static void main(String[] args) {
        String nama = "John Doe";
        int umur = 20;
        float nilai = 9.5f;
        char a = 'a';
        boolean apakahBenar = true;

        System.out.println("Tipe Data String: " + nama.getClass());
        System.out.println("Tipe Data Integer: " + umur.getClass());
        System.out.println("Tipe Data Float: " + nilai.getClass());
        System.out.println("Tipe Data Character: " + a.getClass());
        System.out.println("Tipe Data Boolean: " + apakahBenar.getClass());  
    }
}
